package org.exercises.oop.principles.calculator;

public class Calculator {

  public void multiply(int a, int b) {
    System.out.println(a * b);
  }

  public void multiply(int a, double b) {
    System.out.println(a * b);
  }

  public void multiply(double a, double b) {
    System.out.println(a * b);
  }
}